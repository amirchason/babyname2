def consolidate_names(names_data: List[Dict[str, Any]]) -> List[Dict[str, Any]]:
    """Consolidate name variants into single entries"""
    print(f"Starting consolidation of {len(names_data)} names...")

    # Group names by normalized form
    name_groups = defaultdict(list)

    for name_entry in names_data:
        if not name_entry.get('name'):
            continue

        normalized_key = normalize_name_for_grouping(name_entry['name'])
        name_groups[normalized_key].append(name_entry)

    consolidated_names = []

    for group_key, variants in name_groups.items():
        if len(variants) == 1:
            # Single name, just add it
            consolidated_names.append(variants[0])
            continue

        print(f"Consolidating {len(variants)} variants for '{group_key}': {[v['name'] for v in variants]}")

        # Consolidate multiple variants
        # Choose the most formal/complete name as the primary name
        primary_name = choose_primary_name(variants)

        # Create consolidated entry
        consolidated_entry = create_consolidated_entry(primary_name, variants)
        consolidated_names.append(consolidated_entry)

    print(f"Consolidated {len(names_data)} names into {len(consolidated_names)} entries")
    return consolidated_names

def choose_primary_name(variants: List[Dict[str, Any]]) -> Dict[str, Any]:
    """Choose the most appropriate primary name from variants"""

    # Preference order: longer names, no abbreviations, common spelling
    def name_priority(name_entry):
        name = name_entry['name']

        # Penalize very short names (likely abbreviations)
        if len(name) <= 2:
            return -100

        # Prefer names without dots (not abbreviations)
        if '.' in name:
            return -50

        # Prefer longer names
        length_score = len(name)

        # Prefer names that are more popular (lower rank number = higher priority)
        popularity_score = 10000 - name_entry.get('popularityRank', 10000)

        # Prefer standard spellings for common names
        standard_spellings = {
            'muhammad': 50, 'mohammed': 40, 'mohammad': 30, 'mohamed': 20,
            'abraham': 50, 'ibrahim': 40,
            'joseph': 50, 'yusuf': 40, 'youssef': 30,
            'mary': 50, 'maria': 40, 'maryam': 30,
            'john': 50, 'yahya': 40,
            'michael': 50, 'mikael': 40,
            'david': 50, 'dawud': 40,
            'sarah': 50, 'sara': 40,
            'elizabeth': 50, 'lisa': 30, 'liz': 20, 'beth': 20
        }

        spelling_score = standard_spellings.get(name.lower(), 0)

        return length_score + popularity_score + spelling_score

    # Sort by priority and return the best one
    best_variant = max(variants, key=name_priority)
    return best_variant

def create_consolidated_entry(primary: Dict[str, Any], variants: List[Dict[str, Any]]) -> Dict[str, Any]:
    """Create a consolidated entry from primary name and all variants"""

    # Start with primary name data
    consolidated = primary.copy()

    # Collect all variant names
    variant_names = []
    for variant in variants:
        variant_name = variant['name']
        if variant_name != primary['name']:
            variant_names.append(variant_name)

    # Add variants to the consolidated entry
    if variant_names:
        consolidated['variants'] = variant_names

    # Aggregate gender statistics
    total_male = sum(v.get('gender', {}).get('Male', 0) for v in variants)
    total_female = sum(v.get('gender', {}).get('Female', 0) for v in variants)

    consolidated['gender'] = {
        'Male': total_male,
        'Female': total_female
    }

    # Use the best popularity rank (lowest number)
    best_rank = min(v.get('popularityRank', 999999) for v in variants)
    consolidated['popularityRank'] = best_rank

    # Combine meanings, origins, etc.
    meanings = []
    origins = []

    for variant in variants:
        if variant.get('meaning') and variant['meaning'] not in meanings:
            meanings.append(variant['meaning'])
        if variant.get('origin') and variant['origin'] not in origins:
            origins.append(variant['origin'])

    if meanings:
        consolidated['meaning'] = meanings[0]  # Use the first meaning
        if len(meanings) > 1:
            consolidated['alternateMeanings'] = meanings[1:]

    if origins:
        consolidated['origin'] = origins[0]  # Use the first origin
        if len(origins) > 1:
            consolidated['alternateOrigins'] = origins[1:]

    return consolidated

